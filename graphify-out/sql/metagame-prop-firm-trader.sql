<h2>Sobrevivência > Vitória: A Mentalidade dos 1.000 Trades</h2>
<p>Trader iniciante quer "ganhar". Define meta agressiva, opera com tamanho grande, busca trades com RR alto. Resultado: variância alta, drawdowns frequentes, eventual ruína matemática.</p>
<p>Trader profissional pensa diferente: <strong>"como sobrevivo aos próximos 1.000 trades?"</strong>. Esse reframe muda tudo:</p>

<ul>
<li>Posição menor (mesmo que pareça "tímido")</li>
<li>Setups mais conservadores (recusa A- e B+, opera só A+)</li>
<li>Drawdown auto-imposto antes do limite da firma</li>
<li>Pause obrigatório após perda emocional</li>
<li>Foco em consistência mensal, não em "ganhar muito num dia"</li>
</ul>

<div class="callout callout-blue">
<strong>Reframe de Tom Hougaard:</strong> "Trading não é ganhar. É não perder de formas idiotas." Quem absorve essa filosofia não opera buscando vitória. Opera evitando perda evitável. Resultado matemático: vitória vem como consequência.
</div>

<h2>O Conceito de Edge Real vs Edge Percebido</h2>
<p><strong>Edge real:</strong> vantagem estatisticamente comprovada em 200+ trades, com expectância positiva consistente.</p>
<p><strong>Edge percebido:</strong> setup que "parece funcionar" baseado em 20-30 trades, ou em backtest mal feito, ou em "feeling" do trader.</p>

<p>A maioria dos traders opera edge percebido achando que tem edge real. Resultado: variância natural eventualmente expõe que não havia edge nenhum. Conta zera. Trader troca estratégia. Repete o ciclo.</p>

