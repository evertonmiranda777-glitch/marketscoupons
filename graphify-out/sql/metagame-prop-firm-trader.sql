<tr><td>Expectância positiva (WR × Avg Win &gt; LR × Avg Loss)</td><td>"Geralmente acerto mais que erro"</td></tr>
<tr><td>Funciona em condições de mercado diferentes</td><td>Só funciona em mercado tendência (ou só range)</td></tr>
<tr><td>Robusto a mudanças de regime</td><td>Para de funcionar quando volatilidade muda</td></tr>
