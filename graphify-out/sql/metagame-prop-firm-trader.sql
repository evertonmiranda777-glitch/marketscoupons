<h2>O Caminho Para Funded Sustentável</h2>
<p>O caminho que dá certo:</p>

<ol>
<li><strong>Mês 1-2:</strong> demo, paper trading. Domina plataforma. Conhece regras de 3 firmas em detalhe.</li>
<li><strong>Mês 3:</strong> avaliação 25K (Apex ou FTMO Mini). Foca 100% em sobreviver, não em passar.</li>
<li><strong>Mês 4-5:</strong> se passou, fica em verificação. Repete mesmo estilo. Não muda.</li>
<li><strong>Mês 6:</strong> primeira funded. Tamanho mínimo de posição. Foco em primeiro payout (mesmo que pequeno).</li>
<li><strong>Mês 7-12:</strong> consistência. Payouts mensais documentados. Confiança crescendo proporcionalmente.</li>
<li><strong>Ano 2:</strong> avaliação maior. Multi-firmas. Diversificação real.</li>
</ol>

<p>O caminho que dá errado:</p>

<ol>
<li>Compra Apex 250K no segundo dia de trading</li>
<li>Reprovou em 3 dias por overtrade</li>
<li>Compra outra. Reprova de novo.</li>
<li>3 meses, $600 gastos, zero progresso técnico, frustração extrema</li>
<li>Conclui que "prop firm é golpe" e desiste</li>
</ol>

<div class="callout callout-green">
<strong>O caminho lento é o caminho rápido.</strong> Quem leva 12-18 meses pra atingir funded sustentável passa rápido pelos próximos 5 anos. Quem tenta atalhar em 30 dias geralmente passa 3 anos repetindo o mesmo erro com firmas diferentes.
</div>

