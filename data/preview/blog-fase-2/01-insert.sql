<h2>3. As regras que decidem a corrida</h2>

<h3>3.1 Trailing Drawdown — como funciona em cada uma</h3>

<p>Apex e Bulenox usam o mesmo conceito de Trailing: o limite de perda "segue" o saldo conforme você ganha, até atingir o threshold do plano. Depois disso, o limite fica fixo (não recua mais quando você perde).</p>

<p>Diferenças sutis:</p>
<ul>
<li><b>Apex:</b> threshold = profit target × 1,2. Conta 50K com target $3.000 trava o trailing em $3.600 acima do início.</li>
<li><b>Bulenox:</b> threshold = profit target × 1,0. Mesma conta trava em $3.000 (no momento que você bate o target).</li>
</ul>

<p>A diferença é importante: Bulenox trava o trailing mais cedo, dando mais margem de segurança após você atingir o alvo. Apex força você a manter buffer até $600 acima do target antes do trailing parar.</p>

<h3>3.2 Bulenox tem EOD Drawdown — Apex não</h3>

<div class="callout callout-good">
<b>EOD Drawdown é a maior vantagem da Bulenox para day traders.</b> Em vez de o limite seguir o saldo em tempo real, ele recalcula só no fim do dia. Você pode passar por uma queda intraday violenta e, se fechar o dia em ganho, o limite só sobe daí. Apex obriga você a operar com trailing real-time.
</div>

<h3>3.3 Consistency rule — ambas relaxadas</h3>

<p>Apex tem "consistência sugerida" de 30%: o maior dia de lucro não deve passar 30% do total acumulado. Não é hard rule, mas pode atrasar payout. Bulenox simplesmente não tem essa regra — você pode ter 1 dia de US$ 3.000 em US$ 5.000 e ainda sacar normalmente.</p>

<p>Na prática: <b>Bulenox é mais permissiva</b> para quem aproveita um dia de mercado raro com tudo. Apex força distribuir o lucro.</p>

<h3>3.4 News trading — ponto da Apex</h3>

<p>Apex permite news trading sem restrição. Bulenox tem restrição de ±5 minutos em torno de news de alto impacto (NFP, CPI, FOMC). Pra quem opera fluxo de dados macro, Apex tem vantagem clara.</p>

